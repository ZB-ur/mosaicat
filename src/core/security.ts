import type { PipelineConfig } from './types.js';

export enum TrustLevel {
  Initiator = 0,
  Orchestrator = 1,
  Agent = 2,
  External = 3,
}

export interface SecurityConfig {
  initiatorLogin: string;
  rejectPolicy: 'silent' | 'error';
}

export function loadSecurityConfig(
  pipelineConfig: PipelineConfig,
  initiatorLogin?: string,
): SecurityConfig {
  return {
    initiatorLogin:
      initiatorLogin ??
      pipelineConfig.security.initiator ??
      '',
    rejectPolicy: (pipelineConfig.security.reject_policy as 'silent' | 'error') ?? 'silent',
  };
}

export function isTrustedActor(login: string, config: SecurityConfig): boolean {
  if (!config.initiatorLogin) return false;
  return login === config.initiatorLogin;
}

export function assertTrustedActor(login: string, config: SecurityConfig): void {
  if (!isTrustedActor(login, config)) {
    throw new Error(`Untrusted actor: ${login} (expected: ${config.initiatorLogin})`);
  }
}

export interface StageIssueParams {
  agentId: string;
  agentName: string;
  taskRef: string;
  inputs: string[];
  outputs: string[];
  durationMs?: number;
  usage?: { input_tokens: number; output_tokens: number; cost_usd?: number };
  retryCount?: number;
  hadClarification?: boolean;
  wasRejected?: boolean;
  commitSha?: string;
}

export function buildIssueBody(params: StageIssueParams): string {
  const lines: string[] = [];

  lines.push(`## ${params.agentName} — Stage Report`);
  lines.push('');

  // Execution summary
  const durationStr = params.durationMs != null ? formatDurationForIssue(params.durationMs) : '—';
  lines.push(`| Item | Value |`);
  lines.push(`|------|-------|`);
  lines.push(`| **Agent** | ${params.agentName} (\`${params.agentId}\`) |`);
  lines.push(`| **Run** | \`${params.taskRef}\` |`);
  lines.push(`| **Duration** | ${durationStr} |`);
  if (params.usage) {
    const costStr = params.usage.cost_usd != null ? ` ($${params.usage.cost_usd.toFixed(2)})` : '';
    lines.push(`| **Tokens** | in: ${params.usage.input_tokens.toLocaleString()} / out: ${params.usage.output_tokens.toLocaleString()}${costStr} |`);
  }
  if (params.retryCount && params.retryCount > 0) {
    lines.push(`| **Retries** | ${params.retryCount} |`);
  }

  // Status badges
  const badges: string[] = [];
  if (params.hadClarification) badges.push('🔄 Clarification');
  if (params.wasRejected) badges.push('✏️ Revised after rejection');
  if (params.retryCount && params.retryCount > 0) badges.push(`↻ Retried ${params.retryCount}x`);
  if (badges.length > 0) {
    lines.push('');
    lines.push(badges.join(' · '));
  }

  // Inputs
  lines.push('');
  lines.push('### Inputs');
  if (params.inputs.length > 0) {
    lines.push(params.inputs.map((i) => `- \`${i}\``).join('\n'));
  } else {
    lines.push('_None_');
  }

  // Outputs
  lines.push('');
  lines.push('### Outputs');
  lines.push(params.outputs.map((o) => `- \`${o}\``).join('\n'));

  // Commit link
  if (params.commitSha) {
    lines.push('');
    lines.push(`### Commit`);
    lines.push(`${params.commitSha.slice(0, 7)}`);
  }

  lines.push('');
  lines.push('---');
  lines.push('_Generated by Mosaicat pipeline_');

  return lines.join('\n');
}

function formatDurationForIssue(ms: number): string {
  if (ms < 1000) return `${ms}ms`;
  if (ms < 60000) return `${(ms / 1000).toFixed(1)}s`;
  const min = Math.floor(ms / 60000);
  const sec = Math.round((ms % 60000) / 1000);
  return `${min}m${sec}s`;
}
